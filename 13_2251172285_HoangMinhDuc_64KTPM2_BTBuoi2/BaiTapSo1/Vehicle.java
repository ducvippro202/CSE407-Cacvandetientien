
public class Vehicle {
	protected String Brand;
	protected float MaxSpeed;
	
	public Vehicle()
	{
		
	}

	public String getBrand() {
		return Brand;
	}

	public void setBrand(String brand) {
		Brand = brand;
	}

	public float getMaxSpeed() {
		return MaxSpeed;
	}

	public void setMaxSpeed(float maxSpeed) {
		MaxSpeed = maxSpeed;
	}
	
	

}
